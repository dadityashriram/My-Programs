package com.shriram.switch1;

public class SwitchDemo3 {

	public static void main(String[] args) {

		//int x = 0;
		 //int x = 1;
		//int x = 2;
		int x = 3;

		switch (x) {

		case 0:
			System.out.println(0);

		case 1:
			System.out.println(1);
			break;

		case 2:
			System.out.println(2);

		default:
			System.out.println("Default");
		}

	}

}
